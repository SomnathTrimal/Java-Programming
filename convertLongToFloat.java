class convertLongToFloat
{ 
    public static void main(String[]args)
    {
        Long l1 = 69l;
        Long l2 = 54l;
        Long l3  = 12l;
      
        float f1 = l1.floatValue();
        float f2 = l2.floatValue();
        float f3 = l3.floatValue();
      System.out.println("the float value for the Long value '"+l1+"' is given as : "+f1);  
      System.out.println("the float value for the Long value '"+l2+"' is given as : "+f2);  
      System.out.println("the float value for the Long value '"+l3+"' is given as : "+f3);  



    }


}



